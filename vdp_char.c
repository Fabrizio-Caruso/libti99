void vdpchar(int pAddr, int ch) {
#ifdef ENABLE_TEXT64
    if (nTextRow == 64*23) {
        char64(pAddr, ch);
        return;
    }
#endif

	VDP_SET_ADDRESS_WRITE(pAddr);
	VDPWD=ch;
#ifdef ENABLE_F18A
    if (gColor == 0x800) {
	extern unsigned int conio_scrnCol; // conio_bgcolor.c
	VDP_SET_ADDRESS_WRITE(pAddr-gImage+gColor);
	VDPWD=conio_scrnCol;
    }
#endif
}
